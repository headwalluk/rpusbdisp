function log(msg) {
  process.stderr.write(msg + "\n");
}

/**
 * Scan /proc/fb for "rpusbdisp-fb" and return the device path, or null.
 */
function detectRpusbdispFb() {
  try {
    const data = fs.readFileSync("/proc/fb", "utf8");
    for (const line of data.trim().split("\n")) {
      const match = line.match(/^(\d+)\s+(.+)/);
      if (match && match[2].trim() === "rpusbdisp-fb") {
        return `/dev/fb${match[1]}`;
      }
    }
  } catch (_) {
    // /proc/fb may not exist
  }
  return null;
}

/**
 * Read framebuffer parameters from sysfs.
 */
function readFbParams(fbDev) {
  const fbName = path.basename(fbDev);
  const sysfs = `/sys/class/graphics/${fbName}`;

  const vsizeRaw = fs.readFileSync(`${sysfs}/virtual_size`, "utf8").trim();
  const parts = vsizeRaw.replace("x", ",").split(",");
  const width = parseInt(parts[0], 10);
  const height = parseInt(parts[1], 10);

  const bpp = parseInt(
    fs.readFileSync(`${sysfs}/bits_per_pixel`, "utf8").trim(),
    10
  );

  return { width, height, bpp };
}

/**
 * Render a character into a buffer at (dx, dy) with given scale.
 */
function drawChar(buf, fbWidth, bpp, ch, dx, dy, scale) {
  const glyph = FONT[ch] || FONT["?"] || FONT[" "];
  const bytesPerPixel = bpp / 8;

  for (let row = 0; row < FONT_H; row++) {
    const bits = glyph[row];
    for (let col = 0; col < FONT_W; col++) {
      if (bits & (0x80 >> col)) {
        // Fill a scale x scale block
        for (let sy = 0; sy < scale; sy++) {
          for (let sx = 0; sx < scale; sx++) {
            const px = dx + col * scale + sx;
            const py = dy + row * scale + sy;
            if (px < 0 || px >= fbWidth) continue;
            const offset = (py * fbWidth + px) * bytesPerPixel;
            if (offset + bytesPerPixel > buf.length) continue;

            if (bpp === 16) {
              // RGB565 white = 0xFFFF
              buf.writeUInt16LE(0xffff, offset);
            } else if (bpp === 24) {
              buf[offset] = 0xff;     // B
              buf[offset + 1] = 0xff; // G
              buf[offset + 2] = 0xff; // R
            } else if (bpp === 32) {
              buf[offset] = 0xff;     // B
              buf[offset + 1] = 0xff; // G
              buf[offset + 2] = 0xff; // R
              buf[offset + 3] = 0xff; // A
            }
          }
        }
      }
    }
  }
}

function main() {
  // Determine framebuffer device
  let fbDev;
  if (process.argv[2]) {
    fbDev = process.argv[2];
    log(`Using framebuffer device from argument: ${fbDev}`);
  } else {
    fbDev = detectRpusbdispFb();
    if (fbDev) {
      log(`Auto-detected rpusbdisp framebuffer: ${fbDev}`);
    } else {
      fbDev = "/dev/fb0";
      log(`WARNING: rpusbdisp-fb not found in /proc/fb; defaulting to ${fbDev}`);
    }
  }

  if (!fs.existsSync(fbDev)) {
    log(`ERROR: ${fbDev} does not exist.`);
    process.exit(1);
  }

  const { width, height, bpp } = readFbParams(fbDev);
  log(`Resolution: ${width}x${height}, depth: ${bpp} bpp`);

  const bytesPerPixel = bpp / 8;
  const bufSize = width * height * bytesPerPixel;
  const buf = Buffer.alloc(bufSize, 0); // black background

  const text = "HELLO WORLD";

  // Calculate scale so text fills roughly 1/3 of the width
  const textPixelWidth = text.length * FONT_W;
  const targetWidth = Math.floor(width * 0.6);
  const scale = Math.max(1, Math.floor(targetWidth / textPixelWidth));

  const totalW = text.length * FONT_W * scale;
  const totalH = FONT_H * scale;
  const startX = Math.floor((width - totalW) / 2);
  const startY = Math.floor((height - totalH) / 2);

  log(`Rendering "${text}" with scale ${scale}x...`);

  for (let i = 0; i < text.length; i++) {
    drawChar(buf, width, bpp, text[i], startX + i * FONT_W * scale, startY, scale);
  }

  // Write to framebuffer
  const fd = fs.openSync(fbDev, "w");
  fs.writeSync(fd, buf);
  fs.closeSync(fd);

  log(`Done. "${text}" written to ${fbDev}.`);
}

main();
